// src/stores/course.js
import { ref, computed } from 'vue'
import { defineStore } from 'pinia'
import CourseService from '@/services/courseService'
import EnrollmentService from '@/services/enrollmentService'
import { useAuthStore } from './auth'

export const useCourseStore = defineStore('course', () => {
    // ÏÉÅÌÉú
    const courses = ref([])
    const enrollments = ref([])
    const selectedCourseIds = ref([])
    const isLoading = ref(false)
    const isLoadingFromCache = ref(false)
    const error = ref(null)
    const hasMore = ref(true)
    const lastDoc = ref(null)
    const lastLoadTime = ref(null)

    // Í≥ÑÏÇ∞Îêú ÏÜçÏÑ±
    const selectedCount = computed(() => selectedCourseIds.value.length)

    const selectedCourses = computed(() => {
        return selectedCourseIds.value
            .map(id => courses.value.find(course => course.id === id))
            .filter(Boolean)
    })

    const enrollmentMap = computed(() => {
        const map = new Map()
        enrollments.value.forEach(enrollment => {
            map.set(enrollment.courseId, enrollment)
        })
        return map
    })

    // Í∞ïÏùò Î™©Î°ù Î°úÎìú (Í∞úÏÑ†Îêú Flask ÌîÑÎ°ùÏãú ÏßÄÏõê)
    const loadCoursesFromFirestore = async (forceRefresh = false) => {
        try {
            // Ï∫êÏãú Ï≤¥ÌÅ¨
            if (!forceRefresh && courses.value.length > 0) {
                const cacheAge = Date.now() - (lastLoadTime.value || 0)
                if (cacheAge < 5 * 60 * 1000) { // 5Î∂Ñ
                    console.log('‚úÖ Ï∫êÏãúÎêú Í∞ïÏùò ÏÇ¨Ïö©')
                    return result.courses || []
                }
            }

            isLoading.value = true
            isLoadingFromCache.value = true
            error.value = null

            // Ï∫êÏãúÏóêÏÑú Î®ºÏ†Ä Î°úÎìú ÏãúÎèÑ
            const cachedCourses = CourseService.getCachedCourses()
            if (cachedCourses && cachedCourses.length > 0) {
                courses.value = cachedCourses
                console.log(`üì¶ Ï∫êÏãúÏóêÏÑú ${cachedCourses.length}Í∞ú Í∞ïÏùò Î°úÎìú`)
            }

            // FirebaseÏóêÏÑú Ï†ÑÏ≤¥ Í∞ïÏùò Î°úÎìú
            const result = await CourseService.getCoursesFromFirestore()

            if (result.courses && result.courses.length > 0) {
                courses.value = result.courses
                lastLoadTime.value = Date.now()
                console.log(`‚úÖ FirebaseÏóêÏÑú ${result.courses.length}Í∞ú Í∞ïÏùò Î°úÎìú ÏôÑÎ£å`)
            }

            return courses.value
        } catch (err) {
            console.error('‚ùå Í∞ïÏùò Î°úÎìú Ïã§Ìå®:', err)
            error.value = err.message

            // Í∂åÌïú Ïò§Î•òÏùº Í≤ΩÏö∞ Îπà Î∞∞Ïó¥ Î∞òÌôò
            if (err.code === 'permission-denied') {
                courses.value = []
                return []
            }

            throw err
        } finally {
            isLoading.value = false
            isLoadingFromCache.value = false
        }
    }

    // ÌéòÏù¥ÏßÄÎÑ§Ïù¥ÏÖòÏúºÎ°ú Ï∂îÍ∞Ä Í∞ïÏùò Î°úÎìú
    const loadCoursesWithPagination = async (lastDocument = null) => {
        try {
            if (isLoading.value || !hasMore.value) return

            isLoading.value = true
            error.value = null

            const result = await CourseService.getCoursesWithPagination(
                lastDocument || lastDoc.value
            )

            if (result.courses.length > 0) {
                // Ï§ëÎ≥µ Ï†úÍ±∞ ÌõÑ Ï∂îÍ∞Ä
                const existingIds = new Set(courses.value.map(c => c.id))
                const newCourses = result.courses.filter(c => !existingIds.has(c.id))

                courses.value = [...courses.value, ...newCourses]
                lastDoc.value = result.lastDoc
                hasMore.value = result.hasMore
            } else {
                hasMore.value = false
            }

            return result
        } catch (err) {
            console.error('‚ùå ÌéòÏù¥ÏßÄÎÑ§Ïù¥ÏÖò Î°úÎìú Ïã§Ìå®:', err)
            error.value = err.message
            throw err
        } finally {
            isLoading.value = false
        }
    }

    // Ï∫êÏãú Ï¥àÍ∏∞Ìôî
    const clearCache = () => {
        CourseService.clearCache()
        lastLoadTime.value = null
        courses.value = []
        lastDoc.value = null
        hasMore.value = true
        console.log('üóëÔ∏è Ï∫êÏãú Ï¥àÍ∏∞Ìôî ÏôÑÎ£å')
    }

    // ÏÇ¨Ïö©Ïûê ÏàòÍ∞ï Ï†ïÎ≥¥ Î°úÎìú (ÏµúÏ†ÅÌôî)
    const loadUserEnrollments = async () => {
        try {
            const authStore = useAuthStore()
            const userId = authStore.userId

            if (!userId) {
                enrollments.value = []
                return
            }

            isLoading.value = true

            // CourseServiceÏóêÏÑú ÏµúÏ†ÅÌôîÎêú Î©îÏÑúÎìú ÏÇ¨Ïö©
            const userEnrollments = await CourseService.getUserEnrollmentsWithCourses(userId)

            enrollments.value = userEnrollments

            console.log(`‚úÖ ÏàòÍ∞ï Ï†ïÎ≥¥ Î°úÎìú ÏÑ±Í≥µ: ${enrollments.value.length}Í∞ú`)
        } catch (err) {
            console.error('‚ùå ÏàòÍ∞ï Ï†ïÎ≥¥ Î°úÎìú Ïã§Ìå®:', err)
            error.value = err.message
            enrollments.value = []
        } finally {
            isLoading.value = false
        }
    }

    // ÌäπÏ†ï Í∞ïÏùò Ï†ïÎ≥¥ Í∞ÄÏ†∏Ïò§Í∏∞
    const getCourseById = (courseId) => {
        return courses.value.find(course => course.id === courseId) || null
    }

    // Í∞ïÏùò ÏÉÅÏÑ∏ Ï†ïÎ≥¥ Í∞ÄÏ†∏Ïò§Í∏∞
    const getCourseDetails = async (courseId) => {
        try {
            // Î®ºÏ†Ä Ï∫êÏãúÏóêÏÑú Ï∞æÍ∏∞
            const cached = getCourseById(courseId)
            if (cached) return cached

            // ÏóÜÏúºÎ©¥ FirebaseÏóêÏÑú Î°úÎìú
            const course = await CourseService.getCourseById(courseId)
            if (course) {
                // Ï∫êÏãúÏóê Ï∂îÍ∞Ä
                const existingIndex = courses.value.findIndex(c => c.id === courseId)
                if (existingIndex >= 0) {
                    courses.value[existingIndex] = course
                } else {
                    courses.value.push(course)
                }
            }
            return course
        } catch (err) {
            console.error('Í∞ïÏùò ÏÉÅÏÑ∏ Ï†ïÎ≥¥ Î°úÎìú Ïã§Ìå®:', err)
            throw err
        }
    }

    // ÏàòÍ∞ï ÏÉÅÌÉú ÌôïÏù∏
    const getEnrollmentStatus = (courseId) => {
        const enrollment = enrollmentMap.value.get(courseId)
        return enrollment ? enrollment.status : 'not-enrolled'
    }

    // ÏàòÍ∞ï ÏßÑÎèÑ ÌôïÏù∏
    const getProgress = (courseId) => {
        const enrollment = enrollmentMap.value.get(courseId)
        return enrollment ? enrollment.progress || 0 : 0
    }

    // ÏÑ†ÌÉù Í¥ÄÎ†® Î©îÏÑúÎìú
    const isSelected = (courseId) => {
        return selectedCourseIds.value.includes(courseId)
    }

    const addToSelected = (courseId) => {
        if (!isSelected(courseId) && selectedCount.value < 10) {
            selectedCourseIds.value.push(courseId)
            saveSelectedToStorage()
            return { success: true }
        }
        return { success: false, message: 'ÏµúÎåÄ 10Í∞úÍπåÏßÄ ÏÑ†ÌÉù Í∞ÄÎä•Ìï©ÎãàÎã§.' }
    }

    const removeFromSelected = (courseId) => {
        const index = selectedCourseIds.value.indexOf(courseId)
        if (index > -1) {
            selectedCourseIds.value.splice(index, 1)
            saveSelectedToStorage()
        }
    }

    const clearSelected = () => {
        selectedCourseIds.value = []
        saveSelectedToStorage()
    }

    // Î°úÏª¨ Ïä§ÌÜ†Î¶¨ÏßÄÏóê ÏÑ†ÌÉù Î™©Î°ù Ï†ÄÏû•
    const saveSelectedToStorage = () => {
        localStorage.setItem('selectedCourses', JSON.stringify(selectedCourseIds.value))
    }

    const loadSelectedFromStorage = () => {
        const saved = localStorage.getItem('selectedCourses')
        if (saved) {
            try {
                selectedCourseIds.value = JSON.parse(saved)
            } catch {
                selectedCourseIds.value = []
            }
        }
    }

    // ÏàòÍ∞ï Ïã†Ï≤≠
    const enrollCourse = async (courseId) => {
        try {
            const authStore = useAuthStore()
            const userId = authStore.userId

            const result = await EnrollmentService.enrollCourse(userId, courseId)

            // ÏàòÍ∞ï Ï†ïÎ≥¥ ÏóÖÎç∞Ïù¥Ìä∏
            if (!result.alreadyEnrolled) {
                enrollments.value.push({
                    ...result,
                    course: getCourseById(courseId)
                })
            }

            return result
        } catch (err) {
            console.error('ÏàòÍ∞ï Ïã†Ï≤≠ Ïã§Ìå®:', err)
            throw err
        }
    }

    // ÏÑ†ÌÉùÌïú Í∞ïÏùò ÏùºÍ¥Ñ ÏàòÍ∞ï Ïã†Ï≤≠
    const enrollSelectedCourses = async () => {
        try {
            const authStore = useAuthStore()
            const userId = authStore.userId

            if (!userId) {
                throw new Error('Î°úÍ∑∏Ïù∏Ïù¥ ÌïÑÏöîÌï©ÎãàÎã§')
            }

            isLoading.value = true

            // Ïù¥ÎØ∏ ÏàòÍ∞ï Ï§ëÏù∏ Í∞ïÏùò ÌïÑÌÑ∞ÎßÅ
            const coursesToEnroll = selectedCourseIds.value.filter(id => {
                const status = getEnrollmentStatus(id)
                return status === 'not-enrolled'
            })

            if (coursesToEnroll.length === 0) {
                return {
                    success: true,
                    successCount: 0,
                    failedCount: 0,
                    message: 'Ïù¥ÎØ∏ Î™®Îì† ÏÑ†ÌÉùÌïú Í∞ïÏùòÎ•º ÏàòÍ∞ï Ï§ëÏûÖÎãàÎã§'
                }
            }

            // Î∞∞Ïπò ÏàòÍ∞ï Ïã†Ï≤≠
            const results = await EnrollmentService.enrollMultipleCourses(userId, coursesToEnroll)

            // ÏÑ±Í≥µÌïú ÏàòÍ∞ï Ïã†Ï≤≠ Î°úÏª¨ ÏÉÅÌÉú ÏóÖÎç∞Ïù¥Ìä∏
            results.success.forEach(({ courseId, enrollment }) => {
                enrollments.value.push({
                    ...enrollment,
                    courseId,
                    course: getCourseById(courseId)
                })
            })

            // ÏÑ†ÌÉù Î™©Î°ù Ï¥àÍ∏∞Ìôî
            clearSelected()

            return {
                success: results.failed.length === 0,
                successCount: results.success.length,
                failedCount: results.failed.length,
                message: `${results.success.length}Í∞ú Í∞ïÏùò ÏàòÍ∞ï Ïã†Ï≤≠ ÏôÑÎ£å`
            }
        } catch (err) {
            console.error('ÏùºÍ¥Ñ ÏàòÍ∞ï Ïã†Ï≤≠ Ïã§Ìå®:', err)
            throw err
        }
    }

    // ÏßÑÎèÑ ÏóÖÎç∞Ïù¥Ìä∏
    const updateProgress = async (courseId, progress) => {
        try {
            const authStore = useAuthStore()
            const userId = authStore.userId

            const updated = await CourseService.updateProgress(userId, courseId, progress)

            // Î°úÏª¨ ÏÉÅÌÉú ÏóÖÎç∞Ïù¥Ìä∏
            const enrollment = enrollments.value.find(e => e.courseId === courseId)
            if (enrollment) {
                enrollment.progress = progress
                if (progress >= 100) {
                    enrollment.status = 'completed'
                }
            }

            return updated
        } catch (err) {
            console.error('ÏßÑÎèÑ ÏóÖÎç∞Ïù¥Ìä∏ Ïã§Ìå®:', err)
            throw err
        }
    }

    // ÏàòÍ∞ï ÏÉÅÌÉú ÏóÖÎç∞Ïù¥Ìä∏ (Ï∂îÍ∞ÄÎêú Î©îÏÑúÎìú)
    const updateEnrollmentStatus = async (courseId, status) => {
        try {
            const authStore = useAuthStore()
            const userId = authStore.userId

            if (!userId) return

            // EnrollmentServiceÎ•º ÌÜµÌï¥ ÏÉÅÌÉú ÏóÖÎç∞Ïù¥Ìä∏
            await EnrollmentService.updateEnrollment(userId, courseId, { status })

            // Î°úÏª¨ ÏÉÅÌÉú ÏóÖÎç∞Ïù¥Ìä∏
            const enrollment = enrollments.value.find(e => e.courseId === courseId)
            if (enrollment) {
                enrollment.status = status
                console.log(`üìù ÏàòÍ∞ï ÏÉÅÌÉú ÏóÖÎç∞Ïù¥Ìä∏: ${courseId} -> ${status}`)
            } else {
                // enrollmentÏù¥ ÏóÜÏúºÎ©¥ ÏÉàÎ°ú Ï∂îÍ∞Ä
                enrollments.value.push({
                    courseId,
                    userId,
                    status,
                    progress: status === 'completed' ? 100 : 0,
                    course: getCourseById(courseId)
                })
            }

            return { success: true }
        } catch (err) {
            console.error('ÏàòÍ∞ï ÏÉÅÌÉú ÏóÖÎç∞Ïù¥Ìä∏ Ïã§Ìå®:', err)
            throw err
        }
    }

    // QR ÏΩîÎìúÎ°ú Í∞ïÏùò Ï†ëÍ∑º
    const accessCourseByQR = async (qrData) => {
        try {
            // QR Îç∞Ïù¥ÌÑ∞ ÌååÏã±
            const { courseId, token } = JSON.parse(qrData)

            // ÌÜ†ÌÅ∞ Í≤ÄÏ¶ù
            const isValid = await CourseService.validateQRToken(courseId, token)
            if (!isValid) {
                throw new Error('Ïú†Ìö®ÌïòÏßÄ ÏïäÏùÄ QR ÏΩîÎìúÏûÖÎãàÎã§')
            }

            // Í∞ïÏùò Ï†ïÎ≥¥ Í∞ÄÏ†∏Ïò§Í∏∞
            const course = await getCourseDetails(courseId)
            if (!course) {
                throw new Error('Í∞ïÏùòÎ•º Ï∞æÏùÑ Ïàò ÏóÜÏäµÎãàÎã§')
            }

            return course
        } catch (err) {
            console.error('QR ÏΩîÎìú Ï†ëÍ∑º Ïã§Ìå®:', err)
            throw err
        }
    }

    // Ï∫êÏãú ÏÉÅÌÉú ÌôïÏù∏
    const getCacheStatus = () => {
        return {
            memoryCourses: courses.value.length,
            lastLoadTime: lastLoadTime.value ? new Date(lastLoadTime.value).toLocaleString() : null,
            hasMore: hasMore.value
        }
    }

    return {
        // ÏÉÅÌÉú
        courses,
        enrollments,
        selectedCourseIds,
        isLoading,
        isLoadingFromCache,
        error,
        hasMore,

        // Í≥ÑÏÇ∞Îêú ÏÜçÏÑ±
        selectedCount,
        selectedCourses,
        enrollmentMap,

        // Î©îÏÑúÎìú
        loadCoursesFromFirestore,
        loadCoursesFromFlask: loadCoursesFromFirestore, // Ìò∏ÌôòÏÑ±
        loadCoursesWithPagination,
        clearCache,
        loadUserEnrollments,
        getCourseById,
        getCourseDetails,
        getEnrollmentStatus,
        getProgress,
        isSelected,
        addToSelected,
        removeFromSelected,
        clearSelected,
        clearSelectedCourses: clearSelected, // Ìò∏ÌôòÏÑ±
        saveSelectedToStorage,
        loadSelectedFromStorage,
        enrollCourse,
        enrollSelectedCourses,
        updateProgress,
        updateEnrollmentStatus, // Ï∂îÍ∞ÄÎêú Î©îÏÑúÎìú
        accessCourseByQR,
        getCacheStatus
    }
})